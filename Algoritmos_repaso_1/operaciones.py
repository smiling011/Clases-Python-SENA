"""Oprraciones basicas de aritmetica"""
suma = 45 + 89
resta = 100 - 50
multiplicacion = 2 * 2
division = 100 / 2

print(f"La suma es: {suma}, la resta es : {resta}")
print(f"La multiplicacion es: {multiplicacion}, la division es: {division}")
print(type(suma))
print(type(resta))
print(type(multiplicacion))
print(type(division))

"""Operaciones compleja"""
residuo = 45 % 10
print(f"El residuo es: {residuo}")
diventera = 45 // 3
print(f"El diventera es: {diventera}")