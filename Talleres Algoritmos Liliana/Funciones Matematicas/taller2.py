#Implementar la función pow() para calcular la potencia de un número.

# math.sqrt = raiz
# pow = potencia
# math.sin = seno
# math.cos = coseno 
# math.tan = tangente

num = int(input("Ingrese el numero a calcular: "))
expo = int(input("Ingrese el numero del exponente: "))
pot = pow(num, expo)

print(f"La potencia de {num} es: {pot}") 