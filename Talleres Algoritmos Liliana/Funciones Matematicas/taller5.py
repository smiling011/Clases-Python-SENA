# Implementar la función tan() para calcular la tangente de un ángulo.

# math.sqrt = raiz
# pow = potencia
# math.sin = seno
# math.cos = coseno
# math.tan = tangente :)

import math
ang = int(input(f"Ingresa el angulo: "))
tan = math.tan(math.radians(tan))

print(f"La tangente del angulo de {ang}° es de: {tan}")