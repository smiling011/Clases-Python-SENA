# Implementar la función sqrt() para calcular la raíz cuadrada de un número

# math.sqrt = raiz
# pow = potencia
# math.sin = seno
# math.cos = coseno 
# math.tan = tangente

import math
num = int(input("Ingresa el numero a calcular: "))
raiz = math.sqrt(num)
print(F"La raiz cuadrada de {num} es: {raiz}")
